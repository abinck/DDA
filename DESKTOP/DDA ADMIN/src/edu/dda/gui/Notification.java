    private void initTable() {
        try {
            defaultTableModel = new DefaultTableModel(new String[]{"SL NO", "MESSAGE NAME", "MESSAGE", "POSTED DATE", "POSTED TIME"}, 0);
            notificationModels = DDAClient.getAllNotifications();
            int count = 1;
            for (NotificationModel nm : notificationModels) {
                defaultTableModel.addRow(new String[]{"" + count++, nm.getMsgName(), nm.getMsg(), nm.getNtDate(), nm.getNtTime()});
            }
            notiTB.setModel(defaultTableModel);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

