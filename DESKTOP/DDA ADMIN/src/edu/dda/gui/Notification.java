    private void submitBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBTActionPerformed
        String msgName = msgNameTF.getText();
        String msg = msgTA.getText();
        if ("".equals(msgName)) {
            msgNameTF.setBorder(BorderFactory.createLineBorder(Color.red));
            JOptionPane.showMessageDialog(null, "Enter Message Name", "WARNING", JOptionPane.WARNING_MESSAGE);

        } else if ("".equals(msg)) {
            msgTA.setBorder(BorderFactory.createLineBorder(Color.red));
            JOptionPane.showMessageDialog(null, "Enter Message", "WARNING", JOptionPane.WARNING_MESSAGE);

        } else {
            boolean result = DDAClient.addNotification(msgName, msg);
            if (result) {
                reset();
                initTable();
                writeToConsole("Notification added successfully");
            } else {
                writeToConsole("Failed to add");
            }
        }


    }//GEN-LAST:event_submitBTActionPerformed

    private void resetBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetBTActionPerformed
        reset();
    }//GEN-LAST:event_resetBTActionPerformed

    private void deleteBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBTActionPerformed
        int rowPos = notiTB.getSelectedRow();
        if (rowPos >= 0) {
            int notId = notificationModels.get(rowPos).getId();
            boolean result = DDAClient.deleteNotification(notId);
            if (result) {
                initTable();
                writeToConsole("Notification deleted successfully");
            } else {
                writeToConsole("Failed to delete");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Must Select A Row", "SELECT ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteBTActionPerformed
    public void reset() {
        msgNameTF.setText(null);
        msgTA.setText(null);
        msgNameTF.setBorder(BorderFactory.createLineBorder(null));
        msgTA.setBorder(BorderFactory.createLineBorder(null));
    }

